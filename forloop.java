package day5;

public class forloop {

	public static void main(String[] args) {
		for(int i=146 ; i<=150 ;i++)
		{
			System.out.print(i + " "); 
		}
		System.out.println(" ");
		for(int a=5 ; a>0 ; a--)
		{
			System.out.print(a + " ");
		}
		System.out.println(" ");
		for(int c=1 ; c<10 ; c++)
		{
			if(c%2 != 0)
			{
				System.out.print(c + " ");
			}
		}
		System.out.println(" ");
		for(int d=1 ; d<10 ; d++)
		{
			if(d%2==0)
			{
				System.out.print(d + " ");
			}
	    }
	}
  }
